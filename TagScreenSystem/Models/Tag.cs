namespace TagScreenSystem.Models;

// Модель тега - представляет один тег (датчик) в системе
public class Tag
{
    // Уникальный идентификатор тега (генерируется автоматически)
    // Пример: "fast_tag_0" для быстрых тегов или "tag_0" для обычных
    public string Id { get; set; } = Guid.NewGuid().ToString();
    
    // Тип данных тега: "float" (вещественное число) или "int" (целое число)
    public string Type { get; set; } = "float";
    
    // Текущее значение тега (симулируется случайными числами от 0 до 100)
    public double Value { get; set; }
    
    // Пространство имён тега (целое число, нужно по ТЗ)
    public int Namespace { get; set; }
    
    // ID узла к которому принадлежит тег (строка, нужно по ТЗ)
    public string NodeId { get; set; } = "";
    
    // Время последнего обновления тега (используется как координата X на графиках)
    public DateTime Timestamp { get; set; }
    
    // Интервал обновления тега в миллисекундах
    // 17 мс - для быстрых тегов (30 штук)
    // 50 мс - для обычных тегов (1970 штук)
    public int UpdateIntervalMs { get; set; } = 50;
    
    // Счётчик обновлений за последние 250 мс
    // Используется для прогресс-бара и проверки достижения Queue Size
    // Обнуляется каждые 250 мс
    public int UpdateCounter { get; set; } = 0;
    
    // Флаг быстрого обновления
    // true - тег обновляется каждые 17 мс (30 таких тегов)
    // false - тег обновляется каждые 50 мс (1970 таких тегов)
    public bool IsFastUpdate { get; set; } = false;
}